const overlay = JSON.parse(readFileSync(overlayPath, "utf8"));
for (const [id, tr] of Object.entries(TRANSLATIONS)) {
  const existing = overlay[id] ?? {};
  existing.translations = {
    ...(existing.translations ?? {}),
    literal: tr.literal,
    liberal: tr.liberal,
  };
  overlay[id] = existing;
}
writeFileSync(overlayPath, JSON.stringify(overlay, null, 2) + "\n", "utf8");

const novel = JSON.parse(readFileSync(novelPath, "utf8"));
for (const item of novel) {
  const tr = TRANSLATIONS[String(item.id)];
  if (!tr) continue;
  item.translations = {
    ...(item.translations ?? {}),
    literal: tr.literal,
    liberal: tr.liberal,
  };
}
writeFileSync(novelPath, JSON.stringify(novel, null, 2) + "\n", "utf8");

