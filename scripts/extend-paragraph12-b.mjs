function vref(k,r){ return vIdx.get(`${k}|${r}`); }
function mv(items){
  return items.map(([k,r,m])=>{
    const ref=vref(k,r);
    return ref ? {kanji:k,reading:r,meaning:m,ref} : {kanji:k,reading:r,meaning:m};
  });
}
